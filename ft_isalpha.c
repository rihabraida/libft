#include <stdio.h>
int ft_isalpha(int q)
{
	if( (q <= 'Z' && q >= 'A') || ( q <= 'z' && q >= 'a'))
		return 1;
	else
		return 0;	
		
}
int main()
{
	printf("%d", ft_isalpha(100));
	return 0;
}
