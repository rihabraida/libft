int atoi(const char *str)
{
    int i = 0;
	long res = 0;
	int sign = 0;

	while(str[i] == 32 || ( str[i] >= 9 && str[i] <= 13))
		i++;
	if(str[i] == '-')
	{
		sign = -1;
        i++;
	}
	else if(str[i] == '+')
		i++;
	while( str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
			i++;
	}
	return res * sign;
    
}
