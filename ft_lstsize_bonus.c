#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*p;
	int		i;

	p = lst;
	i = 0;
	if (lst == NULL)
		return (0);
	while (p != NULL)
	{
		p = p->next;
		i++;
	}
	return (i);
}
